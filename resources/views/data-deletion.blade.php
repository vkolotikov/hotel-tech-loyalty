        <h1>Data Deletion Instructions</h1>
        <p class="muted">How to request deletion of your personal data from the Hotel Tech platform.<br>Last updated: 30 May 2026</p>
    </header>

    <p>FDS Cards Ltd ("Hotel Tech", "we") operates the Hotel Tech platform, including the Hotel Tech Loyalty admin (<a href="https://loyalty.hotel-tech.ai">loyalty.hotel-tech.ai</a>), the Hotel Loyalty member mobile app, the Hotel Staff app, and integrations with third-party services including Facebook Messenger. This page explains how individuals can request deletion of personal data that we hold about them.</p>

    <p>For background on what data we collect and why, please read our <a href="/privacy">Privacy Policy</a>.</p>

    <h2>Who is this page for?</h2>
    <p>There are two categories of individuals whose data may be held in our systems, and each has a different deletion path:</p>

    <h3>1. End users (hotel guests, app members, lead form submitters)</h3>
    <p>If you interacted with a hotel that uses our platform — for example, you joined their loyalty program, made a booking through their widget, chatted with their AI assistant, submitted a lead form, sent a Facebook Messenger message to their Page, or downloaded their member app — your personal data is processed by us as a <strong>data processor</strong> on behalf of that hotel (the <strong>data controller</strong>).</p>
    <p><strong>To request deletion, contact the hotel directly first.</strong> They control your data and can usually action the deletion immediately from their admin tools.</p>
    <p>If the hotel cannot be reached or does not respond within a reasonable time (typically 30 days), you may contact us directly using the form below and we will:</p>
    <ul>
        <li>Verify your identity using the contact details on file (email or phone).</li>
        <li>Notify the controlling hotel of your request.</li>
        <li>Delete your data from our systems within <strong>30 days</strong> of verification, except where retention is required by law (e.g. for accounting or fraud prevention).</li>
    </ul>

    <h3>2. Hotel staff or administrators</h3>
    <p>If you are a staff member of a hotel that uses our platform and you have an admin login, you can delete your own account from <strong>Settings → Account → Delete account</strong> in the admin panel. Account deletion removes your personal admin profile but does not delete your hotel's tenant data, which remains under the control of the hotel.</p>

    <h2>How to submit a deletion request to us</h2>

    <div class="callout">
        <p style="margin-top:0;"><strong>Email:</strong> <a href="mailto:info@fds-cards.co.uk?subject=Data%20Deletion%20Request">info@fds-cards.co.uk</a></p>
        <p style="margin-bottom:0;"><strong>Subject line:</strong> <code>Data Deletion Request</code></p>
    </div>

    <p>Please include in your message:</p>
    <ul>
        <li>The hotel or business through which we hold your data (if known).</li>
        <li>The email address or phone number associated with your account or interactions, so we can locate your records.</li>
        <li>Any other identifiers that may help us find your data (e.g. loyalty membership number, booking reference, Facebook user ID if your request relates to Messenger interactions).</li>
        <li>A statement confirming that you are the data subject or are authorised to act on their behalf.</li>
    </ul>

    <h2>What happens after you submit</h2>
    <ol>
        <li>We acknowledge receipt within <strong>5 business days</strong>.</li>
        <li>We verify your identity. We may ask you to confirm details via the email or phone number on file.</li>
        <li>We locate all personal data associated with your identifiers across our platform: chat conversations, lead records, booking history, loyalty membership, mobile app account, and any third-party integration data (e.g. messages received via Facebook Messenger from your Facebook account).</li>
        <li>We delete the data within <strong>30 calendar days</strong> of identity verification.</li>
        <li>We send a confirmation email when deletion is complete.</li>
    </ol>

    <h2>What we may retain</h2>
    <p>Some data may be retained beyond deletion of your account where required by law or for legitimate business purposes:</p>
    <ul>
        <li><strong>Accounting and tax records</strong> (UK law typically requires retention of transaction records for 6 years).</li>
        <li><strong>Fraud prevention records</strong> retained for the minimum necessary period.</li>
        <li><strong>Backup snapshots</strong> may contain your data for up to 90 days before they are overwritten. Data in backups is not actively processed.</li>
        <li><strong>Anonymised analytics data</strong> from which you can no longer be identified.</li>
    </ul>

    <h2>Facebook / Meta data specifically</h2>
    <p>If your deletion request relates to data we hold about you because you sent messages to a hotel's Facebook Page that uses our integration:</p>
    <ul>
        <li>We will delete the chat conversation records, your Facebook Page-Scoped User ID (PSID), and any attached media that was downloaded from Meta's CDN.</li>
        <li>Note that Facebook itself also retains copies of these messages independently. To request deletion from Facebook directly, contact Meta at <a href="https://www.facebook.com/help/contact/507739850846588" target="_blank" rel="noopener">https://www.facebook.com/help/contact/507739850846588</a>.</li>
    </ul>

    <h2>Your rights</h2>
    <p>Under the UK GDPR and EU GDPR you have the right to:</p>
    <ul>
        <li>Request access to your data (Subject Access Request).</li>
        <li>Request rectification of inaccurate data.</li>
        <li>Request deletion ("right to be forgotten") as described above.</li>
        <li>Restrict or object to certain processing.</li>
        <li>Data portability (receive a copy of your data in a machine-readable format).</li>
        <li>Lodge a complaint with the UK Information Commissioner's Office (<a href="https://ico.org.uk/make-a-complaint/" target="_blank" rel="noopener">ico.org.uk</a>) or your local data protection authority.</li>
    </ul>

